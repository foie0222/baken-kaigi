// モックデータを取得する関数
export function getMockRaces(venue?: string, date?: string): Race[] {
  let races = [...mockRacesNakayama, ...mockRacesKyoto];

  if (venue) {
    races = races.filter((r) => r.venue === venue);
  }

  if (date) {
    races = races.filter((r) => r.date === date);
  }

  return races;
}

